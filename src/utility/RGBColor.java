package utility;

/**
 *
 * @author Derek
 */
public class RGBColor {
    
    public float r, g, b;
    
    public RGBColor() {}
    
    public RGBColor(float c) {
        this.r = c;
        this.g = c;
        this.b = c;
    }
    
    public RGBColor(float _r, float _g, float _b) {
        this.r = _r;
        this.g = _g;
        this.b = _b;
    }
    
    
    
    
    
}
